package org.stilkuhni.ui.primitiv;

public class Dot {

    private int coordinateX;
    private int coordinateY;

    public Dot(int coordinateX, int coordinateY) {
        this.coordinateX = coordinateX;
        this.coordinateY = coordinateY;
    }

    public int getX() {
        return coordinateX;
    }

    public int getY() {
        return coordinateY;
    }
}
