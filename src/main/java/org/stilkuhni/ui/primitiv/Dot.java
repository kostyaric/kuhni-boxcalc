package org.stilkuhni.ui.primitiv;

public class Dot {

    private double coordinateX;
    private double coordinateY;

    public Dot(double coordinateX, double coordinateY) {
        this.coordinateX = coordinateX;
        this.coordinateY = coordinateY;
    }

    public double getX() {
        return coordinateX;
    }

    public double getY() {
        return coordinateY;
    }
}
